import Foundation

public class Car {
    
    var make: String
    var model: String
    var year: String
    var millage: Double
    var gasType: String
    var mpg: Double
    
    
    // MARK: Initializer
    public init(make: String, model: String, year: String, millage: Double, gasType: String, mpg: Double) {
        self.make = make
        self.model = model
        self.year = year
        self.millage = millage
        self.gasType = gasType
        self.mpg = mpg
    }
    
    // MARK: Getters & Setters
    
    
    
    
}
